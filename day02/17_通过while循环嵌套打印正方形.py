# 外层 while 步骤流程：
# 1. 定义外层循环的条件变量 i = 0
i = 0

# 2. 外层while 条件(i < 5)：
while i < 5:

    # 3. 内循环处理(先空着，不处理)
    # 内层 while 步骤流程：
    # 3.1 定义内层循环条件变量 j = 0
    j = 0
    # 3.2 内层循环 while j < 5:
    while j < 5:
        # 3.3 打印一个星星，end=''作用指定不换行打印
        print("* ", end='')
        # 3.4 内层循环的条件变量改变
        j += 1

    # 4. 单独打印空行
    print()

    # 5. 外层循环的条件变量改变
    i += 1
