"""
需求：计算1~100之间偶数的累计和
判断一个变量是否为偶数：对2求余，余数为0即为偶数

i = 4
if i % 2 == 0:
    print("偶数")

"""

# 1. 定义条件变量
i = 1
# 2. 设置辅助变量
_sum = 0
# 3. while 条件：
while i <= 100:
    # 3.1 if 判断i是否为偶数
    if i % 2 == 0:
        # 3.1.1 累加
        _sum += i
        # 3.2 条件变量的改变
    i += 1
# 4. 循环外面，打印累加结果
print(_sum)
