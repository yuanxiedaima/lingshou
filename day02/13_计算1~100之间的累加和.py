"""
需求：计算1~100之间的累加和，1 + 2 + 3 …… 100

核心代码分析：
    i = 1
    _sum += i   --> _sum = _sum + i

    i = 2
    _sum += i

    ....

    i = 100
    _sum += i
"""
# 1. 定义条件变量 i, 赋值为1
i = 1
# 2. 定义一个辅助变量，用于保存累计的结果
_sum = 0
# 3. while 条件(i <= 100)：
while (i <= 100):
    # 3.1 完成累加
    _sum += i
    # 3.2 条件变量改变
    i += 1
# 4. 在循环的外面，打印最终的累加结果
print("1到100的累加结果：%d " % _sum)
